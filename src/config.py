def get_battles_data(year_filter=None, type_filter=None, regime_filter=None):
    """获取战役数据，支持筛选"""
    battles = BATTLES_DATA.copy()

    if year_filter:
        battles = [b for b in battles if b['year'] in year_filter]

    if type_filter:
        battles = [b for b in battles if b.get('type') in type_filter]

    if regime_filter:
        battles = [b for b in battles if regime_filter in b.get('sides', [])]

    return battles


